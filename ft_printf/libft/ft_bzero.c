#include <string.h>
#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*m;

	i = 0;
	m = (char *)s;
	while (i < n)
	{
		m[i] = 0;
		i++;
	}
}
